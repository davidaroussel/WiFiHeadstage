def stream_data(port, freq, label):

    bytesPerBuffer = numChannels * numSamples * elementSize
    header = build_header(bytesPerBuffer)

    buffersPerSecond = freq / numSamples
    bufferInterval = 1 / buffersPerSecond

    bytesToSend = generate_data(freq)
    totalBytes = len(bytesToSend)
    bufferIndex = 0

    tcpServer = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    tcpServer.bind(('localhost', port))
    tcpServer.listen(1)

    print(f"[{label}] Waiting for connection on port {port}...")
    tcpClient, address = tcpServer.accept()
    print(f"[{label}] Connected.")

    try:
        while bufferIndex < totalBytes:

            t1 = currentTime()

            tcpClient.sendall(
                header +
                bytesToSend[bufferIndex:bufferIndex + bytesPerBuffer]
            )

            t2 = currentTime()

            while (t2 - t1) < bufferInterval:
                t2 = currentTime()

            bufferIndex += bytesPerBuffer

        print(f"[{label}] Done streaming.")

    except Exception as e:
        print(f"[{label}] Connection error:", e)

    finally:
        tcpClient.close()
        tcpServer.close()

def flush_if_ready(accumulator, sock, header, freq):
    BUFFER_SIZE = 8192

    while len(accumulator) >= BUFFER_SIZE:

        packet = accumulator[:BUFFER_SIZE]
        del accumulator[:BUFFER_SIZE]

        try:
            send_buffer(sock, header, packet, freq)
        except (ConnectionResetError, BrokenPipeError):
            print("OpenEphys disconnected")
            return

def split_data(RAW_DATA_BUFFER, emg_socket, neuro_socket, emg_header, neuro_header):
    global emg_accumulator
    global neuro_accumulator

    words = np.frombuffer(RAW_DATA_BUFFER, dtype='>u2')
    total_words = len(words)

    EMG_WORDS = 32
    WORD_SIZE = 2

    # -----------------------------------
    # Find EMG start indices
    # -----------------------------------
    index_emg_list = [
        i for i, w in enumerate(words)
        if (w & 0x0001) == 1
    ]

    if not index_emg_list:
        neuro_accumulator.extend(RAW_DATA_BUFFER)
        flush_if_ready(neuro_accumulator, neuro_socket, neuro_header)
        return

    consumed_ranges = []

    for index in index_emg_list:

        if index + EMG_WORDS > total_words:
            continue

        start_byte = index * WORD_SIZE
        end_byte = (index + EMG_WORDS) * WORD_SIZE

        emg_block = RAW_DATA_BUFFER[start_byte:end_byte]
        emg_accumulator.extend(emg_block)

        consumed_ranges.append((start_byte, end_byte))

    # -----------------------------------
    # Build Neuro from gaps
    # -----------------------------------
    last_end = 0

    for start_byte, end_byte in sorted(consumed_ranges):

        if last_end < start_byte:
            neuro_accumulator.extend(
                RAW_DATA_BUFFER[last_end:start_byte]
            )

        last_end = end_byte

    if last_end < len(RAW_DATA_BUFFER):
        neuro_accumulator.extend(
            RAW_DATA_BUFFER[last_end:]
        )

    # -----------------------------------
    # Flush when ready
    # -----------------------------------
    flush_if_ready(emg_accumulator, emg_socket, emg_header, Freq_EMG)
    flush_if_ready(neuro_accumulator, neuro_socket, neuro_header, Freq_Neuro)





