def split_accumulate_and_send(raw_buffer,
                              neuro_client,
                              emg_client,
                              neuro_header,
                              emg_header):

    global emg_accumulator
    global neuro_accumulator

    # -----------------------------------
    # Convert to 16-bit unsigned
    # -----------------------------------
    words = np.frombuffer(raw_buffer, dtype='>u2')

    # -----------------------------------
    # Find LSB=1 marker
    # -----------------------------------
    start_index = None
    for i, w in enumerate(words):
        if (w & 0x0001) == 1:
            start_index = i
            break

    if start_index is None:
        return

    EMG_WORDS = 32

    if start_index + EMG_WORDS > len(words):
        return

    # -----------------------------------
    # Split
    # -----------------------------------
    emg_words = words[start_index:start_index + EMG_WORDS]

    neuro_words = np.concatenate((
        words[:start_index],
        words[start_index + EMG_WORDS:]
    ))

    # Convert to little-endian
    emg_bytes = emg_words.astype('<u2').tobytes()
    neuro_bytes = neuro_words.astype('<u2').tobytes()

    # -----------------------------------
    # Append to accumulators
    # -----------------------------------
    emg_accumulator.extend(emg_bytes)
    neuro_accumulator.extend(neuro_bytes)

    # -----------------------------------
    # SEND EMG IF READY
    # -----------------------------------
    while len(emg_accumulator) >= 8192:
        packet = emg_accumulator[:8192]
        del emg_accumulator[:8192]
        emg_client.sendall(emg_header + packet)

    # -----------------------------------
    # SEND NEURO IF READY
    # -----------------------------------
    while len(neuro_accumulator) >= 8192:
        packet = neuro_accumulator[:8192]
        del neuro_accumulator[:8192]
        neuro_client.sendall(neuro_header + packet)

